#!/bin/bash

# Este es un script de ejemplo que utiliza algunas de las opciones más comunes del comando grep

# Buscar una cadena en un archivo
grep "cadena" archivo.txt

# Buscar una cadena en varios archivos
grep "cadena" archivo1.txt archivo2.txt archivo3.txt

# Buscar una cadena ignorando mayúsculas y minúsculas
grep -i "cadena" archivo.txt

# Buscar una cadena y mostrar el número de línea donde se encuentra
grep -n "cadena" archivo.txt


# Buscar una cadena en un directorio y todos sus subdirectorios
grep -r "cadena" directorio/

# Buscar una cadena en un archivo y mostrar solo las líneas que coinciden
